#include "lib_ft.h"

int		is_num(char *str)
{
	int		i;

	i = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	if ('0' <= str[i] && str[i] <= '9')
		return (1);
	return (0);
}