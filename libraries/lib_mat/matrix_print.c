#include <stdio.h>
#include "lib_mat.h"

void	matrix_print(t_mat *m)
{
	int i;
	int j;

	if (m)
	{
		i = -1;
		while(++i < 4)
		{
			j = -1;
			while(++j < 4)
				printf("[%f] ", m->mat[i][j]);
			printf("\n");
		} 
	}
}