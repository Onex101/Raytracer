#include "lib_vec.h"

double	vec3_len_sqr(t_vec3 v)
{
	return ((v.x * v.x) + (v.y * v.y) + (v.z * v.z));
}

double	vec3_len(t_vec3 v)
{
	return(sqrt(vec3_len_sqr(v)));
}