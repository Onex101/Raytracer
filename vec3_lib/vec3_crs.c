#include "vec3_lib.h"

t_vec3	*vec3_crs(t_vec3 *v1, t_vec3 *v2)
{
	if (v1 && v2)
		return(vec3_new(v1->y * v2->z - v1->z * v2->y, v1->z * v2->x - v1->x * v2->z, v1->x * v2->y - v1->y * v2->x));
	return (NULL);
}