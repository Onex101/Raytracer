#include "vec3_lib.h"

void	vec3_sub(t_vec3 *v1, t_vec3 *v2)
{
	if (v1 && v2)
	{
		v1->x -= v2->x;
		v1->y -= v2->y;
		v1->z -= v2->z;
	}
}