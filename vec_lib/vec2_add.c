#include "vec_lib.h"

void	vec2_add(t_vec2 *v1, t_vec2 v2)
{
	v1->x += v2.x;
	v1->y += v2.y;
}

t_vec2	vec2_add_new(t_vec2 v1, t_vec2 v2)
{
	t_vec2 cpy;

	cpy = vec2_cpy(v1);
	vec2_add(&cpy, v2);
	return (cpy);	
}